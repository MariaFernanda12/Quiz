package edu.co.sergio.mundo.vo;

public class Artista {
	private int id;
        private String usuario;
        private String nombre;
        private String apellido;
        private String origen;
        private String curriculum;
        private String distincion;
        private String domicialiado;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public String getCurriculum() {
        return curriculum;
    }

    public void setCurriculum(String curriculum) {
        this.curriculum = curriculum;
    }

    public String getDistincion() {
        return distincion;
    }

    public void setDistincion(String distincion) {
        this.distincion = distincion;
    }

    public String getDomicialiado() {
        return domicialiado;
    }

    public void setDomicialiado(String domicialiado) {
        this.domicialiado = domicialiado;
    }

     
        

}
